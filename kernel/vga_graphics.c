#include "kernel.h"

void vga_copy_buffer(uint8_t* buffer) {
    uint8_t* vga = (uint8_t*)0xA0000;
    memcpy_asm(vga, buffer, 320 * 200);
}

static void vga_outb(uint16_t reg, uint16_t port, uint8_t val) {
    outb(port, reg);
    outb(port + 1, val);
}

void vga_set_mode_13h(void) {
    outb(0x3C2, 0x63);
    vga_outb(0x00, 0x3C4, 0x03);
    vga_outb(0x01, 0x3C4, 0x01);
    vga_outb(0x02, 0x3C4, 0x0F);
    vga_outb(0x03, 0x3C4, 0x00);
    vga_outb(0x04, 0x3C4, 0x0E);
    vga_outb(0x00, 0x3C4, 0x03);
    vga_outb(0x00, 0x3D4, 0x5F);
    vga_outb(0x01, 0x3D4, 0x4F);
    vga_outb(0x02, 0x3D4, 0x50);
    vga_outb(0x03, 0x3D4, 0x82);
    vga_outb(0x04, 0x3D4, 0x54);
    vga_outb(0x05, 0x3D4, 0x80);
    vga_outb(0x06, 0x3D4, 0xBF);
    vga_outb(0x07, 0x3D4, 0x1F);
    vga_outb(0x08, 0x3D4, 0x00);
    vga_outb(0x09, 0x3D4, 0x41);
    vga_outb(0x0A, 0x3D4, 0x00);
    vga_outb(0x0B, 0x3D4, 0x00);
    vga_outb(0x0C, 0x3D4, 0x00);
    vga_outb(0x0D, 0x3D4, 0x00);
    vga_outb(0x0E, 0x3D4, 0x00);
    vga_outb(0x0F, 0x3D4, 0x00);
    vga_outb(0x10, 0x3D4, 0x9C);
    vga_outb(0x11, 0x3D4, 0x0E);
    vga_outb(0x12, 0x3D4, 0x8F);
    vga_outb(0x13, 0x3D4, 0x28);
    vga_outb(0x14, 0x3D4, 0x00);
    vga_outb(0x15, 0x3D4, 0x96);
    vga_outb(0x16, 0x3D4, 0xB9);
    vga_outb(0x17, 0x3D4, 0xA3);
    vga_outb(0x18, 0x3D4, 0xFF);
    vga_outb(0x00, 0x3CE, 0x00);
    vga_outb(0x01, 0x3CE, 0x00);
    vga_outb(0x02, 0x3CE, 0x00);
    vga_outb(0x03, 0x3CE, 0x00);
    vga_outb(0x04, 0x3CE, 0x00);
    vga_outb(0x05, 0x3CE, 0x40);
    vga_outb(0x06, 0x3CE, 0x05);
    vga_outb(0x07, 0x3CE, 0x0F);
    vga_outb(0x08, 0x3CE, 0xFF);
    inb(0x3DA);
    for (int i = 0; i < 16; i++) {
        outb(0x3C0, i);
        outb(0x3C0, i);
    }
    outb(0x3C0, 0x13);
    outb(0x3C0, 0x00);
    outb(0x3C0, 0x14);
    outb(0x3C0, 0x00);
    outb(0x3C0, 0x10);
    outb(0x3C0, 0x41);
    outb(0x3C0, 0x20);
}
